    private static void setupTable() {
        model.addColumn("Nik");
        model.addColumn("Nama");
        model.addColumn("NoTelpon");
        model.addColumn("NoTelpon Darurat");
        model.addColumn("Email");
        model.addColumn("Alamat");
        model.addColumn("Jumlah Rombongan");
        

        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM tabel");

            while(rs.next()) {
                Object[] row = {
                    rs.getString("Nik"),
                    rs.getString("nama"),
                    rs.getString("notelp"),
                    rs.getString("notelp2"),
                    rs.getString("email"),
                    rs.getString("alamat"),
                    rs.getInt("jumlah_rombongan")
                };
                model.addRow(row);
            }

        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();

        }finally {
            closeDb();
        }
    }
    
