    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

                String Nik = NikField.getText();
                String nama = NameField.getText();
                String notelp = NotelpField.getText();
                String notelp2 = Notelp2Field.getText();
                String email = EmailField.getText();
                String alamat = AlamatField.getText();
                String bulan = (String)BulanBox.getSelectedItem();
                int jumlah_rombongan = (int)jumlahrombonganSpinner.getValue();
                int tanggal = (int)tanggalSpinner.getValue();
                int tahun = (int)tahunSpinner.getValue();
                
                
                NamaLabel.setText(nama);
                nimLabel.setText(Nik);
                emailLabel.setText(email);
                notelponLabel.setText(notelp);
                notelpon2Label.setText(notelp2);
                alamatLabel.setText(alamat);
                jumlahrombonganLabel.setText(String.valueOf(jumlah_rombongan));
                TanggalLabel.setText(String.valueOf(tanggal)+" "+bulan+" "+String.valueOf(tahun));
                

                boolean emptyFields = (Nik.equals("") || nama.equals("") || email.equals("") || alamat.equals("") );
                if(emptyFields) {
                    JOptionPane.showMessageDialog(null, "Tolong Isikan Data Terlabih Dahulu");

                }else {
                    try {
                        openDb();
                        s.executeUpdate(
                                "INSERT INTO tabel VALUE ('" + Nik + "','" + nama + "','" + notelp + "','" + notelp2 + "', '" + email + "', '" + alamat + "','" + jumlah_rombongan + "')"
                        );

                        Object[] row = {Nik, nama, notelp, notelp2, email, alamat, jumlah_rombongan};
                        model.addRow(row);

                        JOptionPane.showMessageDialog(null, "Data added!");
                

                    }catch(SQLException | ClassNotFoundException ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());

                    }finally {
                        closeDb();
                    }
                }
    }//GEN-LAST:event_submitButtonActionPerformed

